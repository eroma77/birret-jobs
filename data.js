const getPastDateString = (daysOffset, hoursOffset = 0, minsOffset = 0) => {
  const d = new Date(Date.now() - (daysOffset * 24 * 60 * 60 * 1000) - (hoursOffset * 60 * 60 * 1000) - (minsOffset * 60 * 1000));
  return d.toISOString();
};

